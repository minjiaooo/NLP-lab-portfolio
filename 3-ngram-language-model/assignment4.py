def predict_next_word(partial_sentence, ngram_model):
    """
    Predicts the next word in a partial sentence using the provided n-gram model.

    Args:
        partial_sentence: The partial sentence as a string.
        ngram_model: An instance of the NGramModel class.

    Returns:
        The predicted next word (string) or None if no prediction can be made.
    """

    # transfer words into lower
    tokens = nltk.word_tokenize(partial_sentence)
    tokens = [token.lower() for token in tokens]

    # replace unkown words as 'UNK'
    vocab_set = set(ngram_model.vocabulary)
    processed_tokens = []
    for word in tokens:
        if word in vocab_set:
            processed_tokens.append(word)
        else:
            processed_tokens.append(ngram_model.special_tokens.unknown_token)

    # determine the context length 'n' of the model
    n = 1
    for key in ngram_model.n_gram_counts.keys():
      n = len(key)
      break

    if len(processed_tokens) < n:
        #if the input sentence is too short
        num_padding_needed = n - len(processed_tokens)

        padding = []
        for i in range(num_padding_needed):
            padding.append(ngram_model.special_tokens.start_token)

        context_tokens = padding + processed_tokens
    else:
        # if the input sentence is long enough, just take the last n words
        context_tokens = processed_tokens[-n:]

    # estimate the probabilities of all possible next words
    probabilities = estimate_probabilities(context_tokens, ngram_model)

    # find the word with the maximum probability
    best_word = None
    max_prob = -1.0

    for word, prob in probabilities.items():
        if prob > max_prob:
            max_prob = prob
            best_word = word

    # return the most likely next word
    return best_word

