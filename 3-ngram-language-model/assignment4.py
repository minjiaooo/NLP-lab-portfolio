def write_in_style_ngram(self, passage):
        """
        Takes a passage in, matches it with a style, given a list of
        filenames, and predicts the next word that will appear
        using a bigram model.

        Args:
            passage: A string that contains a passage
            style_file: a list of filenames to be used to determine the style

        Returns:
             single word <string>
             probability associated with the word <float>
             index of "style" it originated from (e.g., 0 for 1st file) <int8>
             probability associated with the style <float>
        """

        # 1. Tokenize and transfer the input passage to lowercase
        tokens = nltk.word_tokenize(passage)
        lower_tokens = []
        for t in tokens:
            lower_tokens.append(t.lower())

        best_style_idx = -1
        best_style_log_prob = -float('inf')

        # 2. Style Classifier: Calculate the log probability of the passage for each model
        for idx, model in enumerate(self.models):
            log_prob_sum = 0.0
            vocab_set = set(model.vocabulary)

            # Handle OOV (Out of Vocabulary) words
            processed_tokens = []
            for w in lower_tokens:
                if w in vocab_set:
                    processed_tokens.append(w)
                else:
                    processed_tokens.append(model.special_tokens.unknown_token)

            # Pad with a start token to calculate bigram probabilities for the sequence
            eval_tokens = [model.special_tokens.start_token] + processed_tokens

            # Sum the log probabilities
            for i in range(len(eval_tokens) - 1):
                context = [eval_tokens[i]]
                target_word = eval_tokens[i+1]

                # Estimate probabilities based on the current context word
                probs = estimate_probabilities(context, model)

                # Get the probability of the target word; use a tiny value to prevent log(0)
                word_prob = probs.get(target_word, 1e-10)
                log_prob_sum += math.log(word_prob)

            # Update the best style if this model has a higher generation probability
            if log_prob_sum > best_style_log_prob:
                best_style_log_prob = log_prob_sum
                best_style_idx = idx

        # 3. Predict the next word using the winning style model
        winning_model = self.models[best_style_idx]
        vocab_set = set(winning_model.vocabulary)

        # Extract the last word of the passage as the context for the Bigram prediction
        if len(lower_tokens) > 0:
            last_word = lower_tokens[-1]
        else:
            last_word = winning_model.special_tokens.start_token

        # Check if the last word is in the winning model's vocabulary
        if last_word not in vocab_set:
            last_word = winning_model.special_tokens.unknown_token

        # Get the probability distribution for the next word
        next_word_probs = estimate_probabilities([last_word], winning_model)

        # Find the word with the highest probability
        word = None
        probability_word = -1.0

        for w, p in next_word_probs.items():
            if p > probability_word:
                probability_word = p
                word = w

        # Assign final return variables exactly as requested by the documentation
        style_file = best_style_idx
        probability_style = best_style_log_prob

        return word, probability_word, style_file, probability_style

