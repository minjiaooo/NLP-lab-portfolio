def estimate_probabilities(context_tokens, ngram_model):
    """
    Estimate the probabilities of a next word using the n-gram counts with k-smoothing

    Args:
        word: next word
        previous_n_gram: A sequence of words of length n
        ngram_model: a structure that contains:
            - n_gram_counts: Dictionary of counts of n-grams
            - n_plus1_gram_counts: Dictionary of counts of (n+1)-grams
            - vocabulary_size: number of words
            - k: positive constant, smoothing parameter

    Returns:
        A dictionary mapping from next words to probability
    """
    probabilities = {}

    context_tuple = tuple(context_tokens)
    context_count = ngram_model.n_gram_counts.get(context_tuple, 0)

    possible_words = ngram_model.vocabulary + [ngram_model.special_tokens.end_token,
                                               ngram_model.special_tokens.unknown_token]
    V = len(possible_words)
    denominator = context_count + ngram_model.k * V

    for word in possible_words:

        n_plus1_gram = context_tuple + (word,)

        n_plus1_count = ngram_model.n_plus1_gram_counts.get(n_plus1_gram, 0)

        probability = (n_plus1_count + ngram_model.k) / denominator

        probabilities[word] = probability

    return probabilities

