def count_n_grams(data, n, special_tokens):
    """
    Count all n-grams in the data

    Args:
        data: List of lists of words
        n: Number of words in a sequence
        special_tokens: A structure that contains:
          - start_token = "<s>"
          - end_token = "<e>"
          - unknown_token = "unk"

    Returns:
        A dictionary that maps a tuple of n-words to its frequency
    """

    # Initialize dictionary of n-grams and their counts
    n_grams = {}

    for sentence in data:
      #add start_token and end token
      tokenized_sentence = [special_tokens.start_token] * (n - 1) + sentence + [special_tokens.end_token]

      #using sliding window to to find n_grams
      for i in range(len(tokenized_sentence) - n + 1):
        n_gram = tuple(tokenized_sentence[i:i+n])
        if n_gram in n_grams:
          n_grams[n_gram] += 1
        else:
          n_grams[n_gram] = 1

    return n_grams

